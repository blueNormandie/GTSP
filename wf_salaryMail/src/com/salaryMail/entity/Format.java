package com.salaryMail.entity;

public class Format {
	private String salaryItems;
	private String earning;
	private String dedcution;

	/**
	 * @return the salaryItems
	 */
	public String getSalaryItems() {
		return salaryItems;
	}

	/**
	 * @param salaryItems
	 *            the salaryItems to set
	 */
	public void setSalaryItems(String salaryItems) {
		this.salaryItems = salaryItems;
	}

	/**
	 * @return the earning
	 */
	public String getEarning() {
		return earning;
	}

	/**
	 * @param earning
	 *            the earning to set
	 */
	public void setEarning(String earning) {
		this.earning = earning;
	}

	/**
	 * @return the dedcution
	 */
	public String getDedcution() {
		return dedcution;
	}

	/**
	 * @param dedcution
	 *            the dedcution to set
	 */
	public void setDedcution(String dedcution) {
		this.dedcution = dedcution;
	}

}
